package com.java.Ass;

public class Prg_3 {

	public static void main(String[] args) {
		
		        int intVar = 10;
		        double doubleVar = 5.5;
		        float floatVar = 3.2f;

		        double result1 = intVar + doubleVar;
		        System.out.println("int and double addition = " + result1);  

		       
		        float result2 = intVar + floatVar;
		        System.out.println("int and float addition = " + result2);  

		        double result3 = doubleVar + floatVar;
		        System.out.println("double and float addition = " + result3);  

		        
		        double result4 = floatVar * doubleVar;
		        System.out.println("float and double multiplication = " + result4);  
		      
		        int result5 = intVar + intVar;
		        System.out.println("int + int addition = " + result5);  // Output will be an int
		    }
		


	

}
